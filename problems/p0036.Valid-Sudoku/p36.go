func isValidSudoku(board [][]byte) bool {
	fill := [3][9][9]bool{}

	var c byte
	var n byte
	var cell int
	for i := 0; i < 9; i++ {
		for j := 0; j < 9; j++ {
			c = board[i][j]
			if c == '.' {
				continue
			}

			n = c - '1'
			cell = i/3*3 + j/3
			if fill[0][i][n] || fill[1][j][n] || fill[2][cell][n] {
				return false
			}

			fill[0][i][n] = true
			fill[1][j][n] = true
			fill[2][cell][n] = true
		}
	}

	return true
}
